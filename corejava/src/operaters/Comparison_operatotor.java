package operaters;

public class Comparison_operatotor { 
	public static void main(String[] args) { 
		System.out.println("Comparision operators"); 
		
		//==,!=,<,>,<=,>= 
		int a=10;
		int b=30;
		
		System.out.println("a equal equal , " + (a==b));
		
		System.out.println("a equal not , " + (a!=b));
		
		System.out.println("a less than , " + (a<b));
		
		System.out.println("a greter than , "+ (a>b));
		
		System.out.println("a less than equal , " + ( a<=b));
		
		System.out.println("a greter than equal , " + (a>=b));
		
		
	}

}

package type_casting;

public class implicit_typecasting { 
	public static void main(String[] args) {
		System.out.println("implicit type casting");  
		
		/* 1- The small data type is convert big data type it is  called implicit type casting.
		 
		 2- In implicit type casting is automatically performed by the compiler. */
		
		int a=10;
		double b= a;
		System.out.println(b);
		
	}

}
